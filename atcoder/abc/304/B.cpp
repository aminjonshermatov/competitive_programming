using namespace std;

void solve() {
  string s; cin >> s;
  for (int i = 3; i < s.size(); ++i) {
    s[i] = '0';
  }
  cout << s << '\n';
}

bool is_multi = false;

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout.tie(nullptr);
  int T = 1;
  if (is_multi) cin >> T;
  for (int tc = 1; tc <= T; ++tc) solve();
  return 0;
}
