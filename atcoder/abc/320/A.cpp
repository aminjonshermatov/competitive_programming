using namespace std;

using i64 = int64_t;

void solve() {
  i64 a, b;
  cin >> a >> b;
  cout << i64(pow(a, b) + pow(b, a)) << '\n';
}

bool is_multi = false;

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout.tie(nullptr);
  int T = 1;
  if (is_multi) cin >> T;
  for (int tc = 1; tc <= T; ++tc) solve();
  return 0;
}
