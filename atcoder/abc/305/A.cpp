using namespace std;

void solve() {
  int n; cin >> n;
  auto lo = n / 5 * 5;
  auto up = lo + 5;
  if (n - lo < up - n) {
    cout << lo << '\n';
  } else {
    cout << up << '\n';
  }
}

bool is_multi = false;

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout.tie(nullptr);
  int T = 1;
  if (is_multi) cin >> T;
  for (int tc = 1; tc <= T; ++tc) solve();
  return 0;
}
