using namespace std;

void solve() {
  int a, b; cin >> a >> b;
  if (2 * a < b) {
    cout << 2 * a << '\n';
  } else {
    cout << a + b << '\n';
  }
}

bool is_multi = true;

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout.tie(nullptr);
  int T = 1;
  if (is_multi) cin >> T;
  for (int tc = 1; tc <= T; ++tc) solve();
  return 0;
}
