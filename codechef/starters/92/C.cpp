using namespace std;

void solve() {
  int w, l; cin >> w >> l;
  if (w & 1 && l & 1) {
    cout << "NO\n";
  } else {
    cout << "YES\n";
  }
}

bool is_multi = true;

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout.tie(nullptr);
  int T = 1;
  if (is_multi) cin >> T;
  for (int tc = 1; tc <= T; ++tc) solve();
  return 0;
}
