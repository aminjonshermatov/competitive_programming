constexpr int md = 1e9 + 7;
using Mint = Modular<std::integral_constant<decay<decltype(md)>::type, md>>;

vector<Mint> fact(1, 1);
vector<Mint> tcaf(1, 1);

Mint C(int n, int k) {
  if (k < 0 || k > n) return 0;
  while ((int) fact.size() < n + 1) {
    fact.push_back(fact.back() * (int) fact.size());
    tcaf.push_back(1 / fact.back());
  }
  return fact[n] * tcaf[k] * tcaf[n - k];
}

template <typename T = int> struct FenwickTreeRangeUpdatePointQuery {
  int n;
  vector<T> bit;

  FenwickTreeRangeUpdatePointQuery() = default;
  explicit FenwickTreeRangeUpdatePointQuery(int n_) : n(n_), bit(n_, T(0))  { }
  template<typename U = T> explicit FenwickTreeRangeUpdatePointQuery(const vector<U> &A) : n(A.size()) , bit(A.size(), T(0)) {
    for (auto idx = 0u; idx < n; ++idx) modify(idx, idx + 1, A[idx]);
  }

  void init(int n_) {
    n = n_;
    bit.assign(n, T(0));
  }

  auto modify(int idx, T val) -> void {
    for (; idx < n; idx = idx | (idx + 1)) bit[idx] += val;
  }

  auto modify(int l, int r, T val) -> void { // [l, r)
    if (l >= r) return;
    modify(l, val);
    modify(r, -val);
  }

  [[nodiscard]] auto query(int idx) const -> T {
    T ret = T(0);
    for (; idx >= 0; idx = (idx & (idx + 1)) - 1) ret += bit[idx];
    return ret;
  }

};

using i64 = int64_t;

void solve() {
  i64 n, k; cin >> n >> k;
  vector<i64> A(n);
  for (auto &a : A) {
    cin >> a;
    if (a == 0 && k > 0) {
      ++a;
      --k;
    }
  }

  FenwickTreeRangeUpdatePointQuery<i64> ft(A);

  for (auto i = n - 2; i >= 0 && k > 0; --i) {
    auto nxt = ft.query(i + 1);
    if (A[i] == nxt) continue ;
    if (A[i] < nxt) {
      auto mn = min(k, nxt - A[i]);
      ft.modify(i, i + 1, mn);
      k -= mn;
    } else {
      auto d = A[i] - nxt;
      auto c = k / d;
      k -= c * d;
      ft.modify(n - c, n, d);
      if (c < n - i && k > 0) {
        ft.modify(n - c - 1, n - c, k);
        k = 0;
      }
    }
  }

  if (k > 0) {
    auto [same, rem] = lldiv(k, n);
    ft.modify(0, n, same);
    ft.modify(n - rem, n, 1);
  }

  vector<Mint> suf(n);
  suf[n - 1] = ft.query(n - 1);
  for (int i = n - 2; i >= 0; --i) {
    suf[i] = suf[i + 1] + ft.query(i);
  }

  dbg(suf);
  Mint ans;
  for (int i = 0; i + 1 < n; ++i) {
    ans += suf[i + 1] * ft.query(i);
  }
  cout << ans << '\n';
}

bool is_multi = true;

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout.tie(nullptr);
  int T = 1;
  if (is_multi) cin >> T;
  for (int tc = 1; tc <= T; ++tc) solve();
  return 0;
}
