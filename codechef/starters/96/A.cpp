using namespace std;

using i64 = int64_t;

void solve() {
  int n, m; cin >> n >> m;
  i64 sm = i64(n) * (n + 1) / 2;
  for (int i = 0; i < m; ++i) {
    int x; cin >> x;
    sm -= x;
  }
  cout << sm << '\n';
}

bool is_multi = true;

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout.tie(nullptr);
  int T = 1;
  if (is_multi) cin >> T;
  for (int tc = 1; tc <= T; ++tc) solve();
  return 0;
}
